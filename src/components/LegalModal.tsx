import { useEffect, useState } from 'react'

export type LegalTab = 'privacy' | 'terms'

interface LegalModalProps {
  isOpen: boolean
  onClose: () => void
  initialTab?: LegalTab
}

export default function LegalModal({ isOpen, onClose, initialTab = 'privacy' }: LegalModalProps) {
  const [tab, setTab] = useState<LegalTab>(initialTab)

  useEffect(() => {
    if (isOpen) setTab(initialTab)
  }, [isOpen, initialTab])

  useEffect(() => {
    if (!isOpen) return
    const onKey = (e: KeyboardEvent) => { if (e.key === 'Escape') onClose() }
    window.addEventListener('keydown', onKey)
    return () => window.removeEventListener('keydown', onKey)
  }, [isOpen, onClose])

  if (!isOpen) return null

  return (
    <div className="kp-privacyOverlay" onClick={onClose}>
      <div className="kp-privacyModal" onClick={e => e.stopPropagation()}>
        <button
          className="kp-privacyModal__close"
          onClick={onClose}
          aria-label="Закрыть"
          type="button"
        >
          <svg viewBox="0 0 24 24" width="20" height="20" fill="none" stroke="currentColor" strokeWidth="2" strokeLinecap="round" strokeLinejoin="round">
            <line x1="18" y1="6" x2="6" y2="18" />
            <line x1="6" y1="6" x2="18" y2="18" />
          </svg>
        </button>

        <div className="kp-legalModal__tabs">
          <button
            type="button"
            className={`kp-legalModal__tab ${tab === 'privacy' ? 'kp-legalModal__tab--active' : ''}`}
            onClick={() => setTab('privacy')}
          >
            Политика конфиденциальности
          </button>
          <button
            type="button"
            className={`kp-legalModal__tab ${tab === 'terms' ? 'kp-legalModal__tab--active' : ''}`}
            onClick={() => setTab('terms')}
          >
            Условия использования
          </button>
        </div>

        {tab === 'privacy' ? (
          <>
            <h2 className="kp-privacyModal__title">Политика конфиденциальности</h2>
            <p className="kp-privacyModal__updated">Последнее обновление: 7 июня 2026 г.</p>
            <div className="kp-privacyModal__content">
              <section>
                <h3>1. Какие данные мы собираем</h3>
                <p>
                  При регистрации мы храним email, логин (handle), пароль (в виде криптографического хеша) и
                  дату создания аккаунта. По желанию вы можете добавить аватар, баннер, краткое био и
                  избранные жанры — эти данные видны другим пользователям в вашем профиле.
                </p>
              </section>

              <section>
                <h3>2. Как используются данные</h3>
                <p>
                  Email используется для входа и восстановления доступа. Логин — для упоминаний, друзей и
                  ссылки на ваш профиль. История просмотров, оценки и избранное хранятся локально на сервере
                  и нужны для работы соответствующих функций. Сообщения в чате комнат и личные сообщения
                  передаются другим участникам беседы.
                </p>
              </section>

              <section>
                <h3>3. Доступ администрации к сообщениям</h3>
                <p>
                  Администраторы сервиса имеют технический доступ к содержимому чатов комнат и личных
                  сообщений всех пользователей. Этот доступ используется исключительно для модерации,
                  расследования жалоб, борьбы со спамом и нарушениями правил сервиса. Сообщения не
                  передаются третьим лицам и не используются в рекламных или маркетинговых целях.
                  Регистрируясь и используя чат, вы соглашаетесь с этим условием.
                </p>
              </section>

              <section>
                <h3>4. Данные гостей</h3>
                <p>
                  Гостевой аккаунт не требует email и пароля. Никнейм, выбранный гостем, существует только
                  на время сессии и не сохраняется после выхода.
                </p>
              </section>

              <section>
                <h3>5. Третьи стороны</h3>
                <p>
                  Каталог фильмов и сериалов работает на основе открытого API <strong>TMDB</strong> —
                  туда не передаются ваши персональные данные. Воспроизведение видео может идти через
                  внешние сервисы (RuTube, VK Видео, прямые ссылки) — их работа регулируется
                  политиками этих площадок.
                </p>
              </section>

              <section>
                <h3>6. Файлы cookie и хранилище</h3>
                <p>
                  Мы используем токены авторизации и локальное хранилище браузера, чтобы вы оставались
                  залогинены и видели сохранённые настройки. Никаких рекламных или отслеживающих cookie мы
                  не устанавливаем.
                </p>
              </section>

              <section>
                <h3>7. Удаление аккаунта</h3>
                <p>
                  Вы можете удалить свой аккаунт в настройках профиля. После удаления личные данные,
                  сообщения и история просмотров стираются и не подлежат восстановлению.
                </p>
              </section>

              <section>
                <h3>8. Безопасность</h3>
                <p>
                  Пароли хранятся в хешированном виде и никогда не передаются в открытом виде. Мы стараемся
                  применять разумные технические и организационные меры защиты, однако ни один сервис в
                  интернете не может гарантировать абсолютную безопасность.
                </p>
              </section>

              <section>
                <h3>9. Изменения политики</h3>
                <p>
                  Мы можем периодически обновлять эту политику. Дата последнего обновления указана в начале
                  документа. Существенные изменения будут анонсированы внутри сервиса.
                </p>
              </section>

              <section>
                <h3>10. Контакты</h3>
                <p>
                  По вопросам приватности и обработки данных свяжитесь с администрацией сервиса через
                  внутренние сообщения с пометкой «Privacy».
                </p>
              </section>
            </div>
          </>
        ) : (
          <>
            <h2 className="kp-privacyModal__title">Условия использования / Пользовательское соглашение</h2>
            <p className="kp-privacyModal__updated">Последнее обновление: 7 июня 2026 г.</p>
            <div className="kp-privacyModal__content">
              <section>
                <h3>1. Общие положения</h3>
                <p>
                  Настоящее соглашение регулирует использование сервиса <strong>ВместеКино</strong> —
                  платформы для совместного онлайн-просмотра фильмов и сериалов с друзьями. Регистрируясь
                  или продолжая пользоваться сервисом в гостевом режиме, вы подтверждаете, что прочитали
                  эти условия и согласны их соблюдать.
                </p>
              </section>

              <section>
                <h3>2. Учётная запись</h3>
                <p>
                  Вы обязуетесь предоставлять достоверные данные при регистрации, не передавать пароль
                  третьим лицам и нести ответственность за все действия, совершённые под вашим аккаунтом.
                  Один человек может иметь только один основной аккаунт. Один и тот же email и логин не
                  могут быть зарегистрированы повторно.
                </p>
              </section>

              <section>
                <h3>3. Возрастные ограничения</h3>
                <p>
                  Сервис предназначен для пользователей старше 12 лет. Отдельные фильмы и сериалы могут
                  иметь возрастные ограничения, установленные их правообладателями (16+, 18+). Вы
                  обязуетесь соблюдать эти ограничения.
                </p>
              </section>

              <section>
                <h3>4. Правила поведения</h3>
                <p>
                  Запрещается: оскорблять других пользователей, разжигать ненависть, публиковать спам,
                  рекламу, ссылки на вредоносное ПО или контент, нарушающий законодательство РФ.
                  Запрещается выдавать себя за других людей, обходить блокировки и иными способами
                  препятствовать работе сервиса.
                </p>
              </section>

              <section>
                <h3>5. Контент и авторские права</h3>
                <p>
                  Сервис не размещает видеоконтент самостоятельно. Воспроизведение происходит через
                  открытые источники (RuTube, VK Видео и публичные ссылки), либо через
                  пользовательские ссылки. Ответственность за легальность контента, публикуемого по
                  ссылкам, несёт пользователь или соответствующая площадка-источник. Информация о фильмах
                  (постеры, описания, рейтинги) предоставлена сервисом <strong>TMDB</strong>.
                </p>
              </section>

              <section>
                <h3>6. Пользовательский контент</h3>
                <p>
                  Сообщения в чатах, отзывы, аватары, баннеры и другой контент, добавляемый пользователем,
                  остаются его собственностью. Однако, размещая контент, вы предоставляете сервису
                  неисключительное право показывать его другим пользователям в рамках работы сервиса.
                </p>
              </section>

              <section>
                <h3>7. Модерация и блокировки</h3>
                <p>
                  Администрация вправе удалять сообщения, ограничивать доступ к функциям или блокировать
                  аккаунты, нарушающие правила сервиса, без предварительного уведомления. В отдельных
                  случаях нарушения могут быть переданы в правоохранительные органы.
                </p>
              </section>

              <section>
                <h3>8. Доступность сервиса</h3>
                <p>
                  Сервис предоставляется «как есть». Мы не гарантируем бесперебойной работы и не несём
                  ответственности за технические сбои, потерю данных или невозможность воспроизведения
                  отдельных источников видео.
                </p>
              </section>

              <section>
                <h3>9. Ограничение ответственности</h3>
                <p>
                  Сервис не несёт ответственности за прямой или косвенный ущерб, возникший в результате
                  использования или невозможности использования сервиса, включая действия третьих лиц,
                  внешних видеоплощадок и других пользователей.
                </p>
              </section>

              <section>
                <h3>10. Изменения условий</h3>
                <p>
                  Мы можем обновлять эти условия. Существенные изменения анонсируются внутри сервиса. Если
                  вы продолжаете пользоваться сервисом после публикации обновлённой версии, считается, что
                  вы приняли изменения.
                </p>
              </section>

              <section>
                <h3>11. Прекращение использования</h3>
                <p>
                  Вы можете в любой момент удалить свой аккаунт через настройки профиля. После удаления
                  личные данные, сообщения и история действий стираются и не подлежат восстановлению.
                </p>
              </section>
            </div>
          </>
        )}

        <div className="kp-privacyModal__footer">
          <button
            className="kp-privacyModal__ok"
            onClick={onClose}
            type="button"
          >
            Понятно
          </button>
        </div>
      </div>
    </div>
  )
}
